; lcm in R0
  AREA RESET, CODE, READONLY
	ENTRY

	MOV	R0,#00;
	MOV R3,#03;
	MOV R4,#02;
	
FIND_LCM	
	ADD R0,R0,#01;
	MOV R5,#01;
	MOV R1,R0;
	MOV R2,R3;
	B FIND_MOD;

FIND_MOD	
	SUBS R1,R1,R2; 
	BEQ CHECK_NEXT_NUM;
	BPL FIND_MOD;
	;remainder is in R1
	B FIND_LCM;

CHECK_NEXT_NUM
	CMP R5,#0;
	BEQ FOUND_LCM;
	MOV R1,R0;
	MOV R2,R4;
	SUB R5,R5,#01;
	B FIND_MOD;

FOUND_LCM	B FOUND_LCM;
	END
